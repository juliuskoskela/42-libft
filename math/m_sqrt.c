float		m_sqrt(float n)
{
	float	xhalf;
	int		i;

	xhalf = 0.5f * n;
	i = *(int *)&n;
	i = 0x5f3759df - (i >> 1);
	n = *(float *)&i;
	n = n * (1.5f - xhalf * n * n);
	return (1 / n);
}
