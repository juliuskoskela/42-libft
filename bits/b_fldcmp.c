#include "../libft.h"

int				b_fldcmp(t_field **f1, t_field **f2)
{
	uint64_t		i;

	i = 0;
	if (!(*f1) || !(*f2))
		return (-1);
	while (i < 4)
	{
		if ((*f1)->row[i] != (*f2)->row[i])
			return (0);
		i++;
	}
	return (1);
}
