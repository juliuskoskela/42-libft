#include "../../inc/libft.h"

t_mtx4			g_translate(t_vct4 vtx)
{
	t_mtx4		out;

	out.name = s_dup("Tv");
	out.v1 = g_vct4(1.0, 0.0, 0.0, vtx.x);
	out.v2 = g_vct4(0.0, 1.0, 0.0, vtx.y);
	out.v3 = g_vct4(0.0, 0.0, 1.0, vtx.z);
	out.v4 = g_vct4(0.0, 0.0, 0.0, vtx.w);
	return (out);
}

