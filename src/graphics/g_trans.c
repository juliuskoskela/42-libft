#include "../../inc/libft.h"

void			g_trans(t_mtx *mtx, t_mtx *vtx)
{
	mtx->this[0] = 1.0;
	mtx->this[3] = vtx->this[0];
	mtx->this[5] = 1.0;
	mtx->this[7] = vtx->this[1];
	mtx->this[10] = 1.0;
	mtx->this[11] = vtx->this[2];
	mtx->this[15] = 1.0;
}

