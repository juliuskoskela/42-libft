#include "../../inc/libft.h"

t_mtx4			g_scale(double scale)
{
	t_mtx4		out;

	out.name = s_dup("S");
	out.v1 = g_vct4(scale, 0.0, 0.0, 0.0);
	out.v2 = g_vct4(0.0, scale, 0.0, 0.0);
	out.v3 = g_vct4(0.0, 0.0, scale, 0.0);
	out.v4 = g_vct4(0.0, 0.0, 0.0, scale);
	return (out);
}

