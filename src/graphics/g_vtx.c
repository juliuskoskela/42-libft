#include "../../inc/libft.h"

t_mtx		*g_vtx(double x, double y, double z, size_t index)
{
	t_mtx	*out;

	out = mtx_new(c_itoa(index), 1, 4);
	out->this[0] = x;
	out->this[1] = y;
	out->this[2] = z;
	out->this[3] = 1.0;
	return (out);
}

