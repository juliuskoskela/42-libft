#include "../../libft.h"

uint64_t			b_highest(uint64_t nb, uint64_t size)
{
	uint64_t		i;

	i = size - 1;
	while (i > 0)
	{
		if (((nb >> i) & 1) == 1)
			return (i + 1);
		i--;
	}
	return (0);
}
