#include "../../inc/libft.h"

void				s_swp(char *a, char *b)
{
	char	out;

	out = *a;
	*a = *b;
	*b = out;
}

/*
**  ----------------------------------------------------------------------------
**
**	S_swp
**
**	Swap characters passed as pointers `a` and `b`.
**
**  ----------------------------------------------------------------------------
*/
