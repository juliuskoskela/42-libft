#include "../../libft.h"

int				s_lenc(char *s, char c)
{
	uint64_t	i;

	i = 0;
	while (s[i] != c && s[i])
		i++;
	return (i);
}

/*
**  ----------------------------------------------------------------------------
**
**	S_lenc
**
**	String lenght (c); returns the lenght until the first occurence of `c` in
**	the null terminated string `s`.
**
**  ----------------------------------------------------------------------------
*/
