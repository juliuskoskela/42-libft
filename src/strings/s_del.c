#include "../../inc/libft.h"

void		s_del(char **as)
{
	if (as != NULL)
	{
		free(*as);
		*as = NULL;
	}
}

/*
**  ----------------------------------------------------------------------------
**
**	S_del
**
**	String delete; delete string by freeing a pointer to the string passed
**	by reference. Does not wipe the contents!
**
**  ----------------------------------------------------------------------------
*/
