#include "../../inc/libft.h"

int			is_lower(int c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	Is_lower
**
**	Returns 1 if `c` is lowercase.
**
**  ----------------------------------------------------------------------------
*/
