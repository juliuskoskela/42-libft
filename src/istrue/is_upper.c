#include "../../inc/libft.h"

int			is_upper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	Is_upper
**
**	Returns 1 if `c` is uppercase.
**
**  ----------------------------------------------------------------------------
*/
