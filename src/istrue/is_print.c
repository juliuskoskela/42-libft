#include "../../inc/libft.h"

int			is_print(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	Is_print
**
**	Returns 1 if `c` is a printable character.
**
**  ----------------------------------------------------------------------------
*/
