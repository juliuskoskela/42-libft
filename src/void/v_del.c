#include "../../libft.h"

void		v_del(void **ap)
{
	if (ap && *ap)
	{
		free(*ap);
		*ap = NULL;
	}
}

/*
**  ----------------------------------------------------------------------------
**
**	V_del
**
**	Void memory delete; frees the pointer `ap` passed by reference and
**	and sets it to `NULL`.
**
**  ----------------------------------------------------------------------------
*/
