#include "../../inc/libft.h"

void		*v_alloc(uint64_t size)
{
	void	*p;

	p = malloc(size);
	if (p)
		v_bzero(p, size);
	return (p);
}

/*
**  ----------------------------------------------------------------------------
**
**	V_alloc
**
**	Void memory allocation; allocates `size` bytes of memory and initializes
**	the memory.
**
**  ----------------------------------------------------------------------------
*/
