#include "../../inc/libft.h"

double			m_sin(double x, int64_t p)
{
	double		sum;
	int64_t		i;
	int64_t		j;

	sum = 0;
	x = x * (PI / 180);
	i = 1;
	j = 1;
	while (i <= p)
	{
		if (i % 2 != 0)
			sum = sum + m_pow(x, j) / m_fac(j);
		else
			sum = sum - m_pow(x, j) / m_fac(j);
		i++;
		j = j + 2;
	}
	return (sum);
}

