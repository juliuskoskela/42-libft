#include "../../libft.h"

float			m_sin(float x, int64_t p)
{
	float		q;
	float		sum;
	int64_t	i;
	int64_t	j;

	q = x;
	sum = 0;
	x = x * (3.1415 / 180);
	i = 1;
	j = 1;
	while (i <= p)
	{
		if (i % 2 != 0)
			sum = sum + m_pow(x, j) / m_fac(j);
		else
			sum = sum - m_pow(x, j) / m_fac(j);
		i++;
		j = j + 2;
	}
	return (sum);
}

