#include "../../libft.h"

uint64_t		m_fac(uint64_t n)
{
	uint64_t	i;
	uint64_t	fac;

	i = 1;
	fac = 1;
	while (i <= n)
	{
		fac = fac * i;
		i++;
	}
	return (fac);
}

