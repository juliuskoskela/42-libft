#include "../../inc/libft.h"

int					m_fsign(long double n)
{
	if (n < 0)
		return (-1);
	else if (n > 0)
		return (1);
	else
		return (0);
}

/*
**  ----------------------------------------------------------------------------
**
**	M_fsign
**
**	Return the sign of float `n`. If `n` is zero returns 0.
**
**  ----------------------------------------------------------------------------
*/
