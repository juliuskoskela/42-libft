#include "../../inc/libft.h"

void				*st_pop(t_stack **head)
{
	void			*res;
	t_stack			*tmp;

	if (*head == NULL)
		return (NULL);
	res = (*head)->content;
	tmp = *head;
	*head = (*head)->next;
	free(tmp);
	return (res);
}

