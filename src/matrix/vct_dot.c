#include "../../inc/libft.h"

double			vct_dot(double *a, double *b, size_t size)
{
	double		out;
	size_t		i;

	i = 0;
	out = 0.0;
	while (i < size)
	{
		out += a[i] * b[i];
		i++;
	}
	return (out);
}

/*
**  ----------------------------------------------------------------------------
**
**	vct_dot
**
**	Dot-product of vector a * b.
**
**  ----------------------------------------------------------------------------
*/
