#include "../../inc/libft.h"

double			*vct_cross(double *a, double *b, size_t size)
{
	double		*out;
	size_t		i;

	i = 0;
	out = (double *)v_alloc(sizeof(double) * size);
	out[0] = (a[1] * b[2]) - (a[2] * b[1]);
	out[1] = (a[2] * b[0]) - (a[0] * b[2]);
	out[2] = (a[0] * b[1]) - (a[1] * b[0]);
	return (out);
}

