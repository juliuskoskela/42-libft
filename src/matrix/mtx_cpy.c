#include "../../inc/libft.h"

void			mtx_cpy(t_mtx *dest, t_mtx *orig)
{
	size_t		i;

	i = 0;
	while (i < dest->x * dest->y)
	{
		dest->this[i] = orig->this[i];
		i++;
	}
}

