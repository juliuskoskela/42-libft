#include "../../inc/libft.h"

void			mtx_get_col(double *col, t_mtx *mtx, size_t index)
{
	size_t		i;
	size_t		j;

	i = index;
	j = 0;
	while (i < mtx->x * mtx->y)
	{
		col[j] = mtx->this[i];
		i += mtx->x;
		j++;
	}
}

/*
**  ----------------------------------------------------------------------------
**
**	Mtx_get_col
**
**	Get a column `col` of matrix `mtx` at `index`.
**
**  ----------------------------------------------------------------------------
*/
