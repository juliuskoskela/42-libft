#include "../../inc/libft.h"

void		p_nbr(int n)
{
	if (n == -2147483648)
		p_str("-2147483648");
	else if (n < 0)
	{
		p_char('-');
		p_nbr(-n);
	}
	else if (n >= 10)
	{
		p_nbr(n / 10);
		p_char(n % 10 + '0');
	}
	else
		p_char(n + '0');
}

/*
**  ----------------------------------------------------------------------------
**
**	P_nbr
**
**	Print number `n` in ascii characters.
**
**  ----------------------------------------------------------------------------
*/
